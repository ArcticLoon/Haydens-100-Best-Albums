# Function to format the text into the desired output
def format_text(input_text):
    items = input_text.strip().split("\n")
    formatted_items = []

    for item in items:
        title, artist = item.strip().split(" ; ")
        name = f"{title} - {artist}"
        image_name = f"{title}.png"
        formatted_item = f"{{id: '{title}', name: '{name}', image: '{image_name}'}},"
        formatted_items.append(formatted_item)

    # Join the formatted items into a single string with newlines
    formatted_output = "\n".join(formatted_items)

    return formatted_output

# Generate the formatted text
formatted_text = format_text(input_text)

# Write the formatted text to a file
with open("formatted_output.txt", "w") as file:
    file.write(formatted_text)

print("Formatted text document created successfully.")
